package jdbc;

public class watch {
	
	private int watch_id;
	private String watch_name;
	private String brand;
	private double price;
	
	public int getWatch_id() {
		return watch_id;
	}
	public void setWatch_id(int watch_no) {
		this.watch_id = watch_no;
	}
	public String getWatch_name() {
		return watch_name;
	}
	public void setWatch_name(String watch_name) {
		this.watch_name = watch_name;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	
	
	

}
